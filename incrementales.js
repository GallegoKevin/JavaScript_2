console.log('// INCREMENTALES:');

let counter = 0;

// Incremento
counter++; 
console.log("Se suma [1] a counter [0]  : " + counter); // Output: 1

counter += 5; 
console.log("Al ultimo resultado de counter que es [1] se suma [5] : "+ counter); 

// Decremento
counter--; 
console.log("Al ultimo resultado de counter se resta [-1] : "+ counter);

counter -= 3;
console.log("Al ultimo resultado de counter se resta [-3] : "+ counter ); 
